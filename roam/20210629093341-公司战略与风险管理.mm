<p>公司战略的基本概念
</p>
</body>
</html>
</richcontent>
<font NAME="SansSerif" SIZE="14"/>
<richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
<html>
<head>
</head>
<body>
<ol class="org-ol">
<li><a id="org206b9cf"></a>公司战略的定义<br />
<ul class="org-ul">
<li>传统概念: 计划性、全局性、长期性</li>
<li>现代概念: 应变性、竞争性、风险性</li>
</ul>
</li>
<li><a id="orgf34847e"></a>公司的使命与目标<br />
<ul class="org-ul">
<li>公司的使命
<ol class="org-ol">
<li>公司目的: 营利、为其所有者带来经济价值</li>
<li>公司宗旨: 经营业务范围</li>
<li>经营哲学: 价值观、基本信念、行为准则、企业文化</li>
</ol></li>
<li>公司的目标
<ol class="org-ol">
<li>财务目标体系: 定量指标，主要体现在财务指标上</li>
<li>战略目标体系: 定性指标，主要体现于在什么方面由于竞争对手，使企业获得竞争优势</li>
</ol></li>
<li>公司战略的层次
<ol class="org-ol">
<li>总体战略: 选择经营范围，合理配置企业经营所需资源</li>
<li>业务单位战略: 建立竞争优势，有效控制资源的分配和使用</li>
<li>职能战略: 配置企业内部资源，发挥协同效应</li>
</ol></li>
</ul>
</li>
</ol>
</body>
